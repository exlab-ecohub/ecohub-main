    RECYCLABLE_TYPES,
    RECYCLING_POINTS_RECYCLABLE_TYPES;

CREATE TABLE ROLES
(
    id   bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    name varchar(20) UNIQUE                                  NOT NULL
);

CREATE TABLE USERS
(
    id       bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    username varchar(20) UNIQUE                                  NOT NULL,
    password varchar                                             NOT NULL,
    email    varchar(50) UNIQUE,
    role_id  bigint REFERENCES ROLES (id) ON DELETE CASCADE      NOT NULL
);

CREATE TABLE TOKENS
(
    id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    refresh_token varchar UNIQUE                                      NOT NULL,
    token_type    varchar                                             NOT NULL,
    token_purpose varchar                                             NOT NULL,
    remember_me   boolean,
    revoked       boolean,
    expired       boolean,
    user_id       bigint REFERENCES USERS (id) ON DELETE CASCADE      NOT NULL
);

CREATE TABLE IF NOT EXISTS FEEDBACKS
(
    feedback_id serial NOT NULL,
    email character varying,
    message_topic character varying,
    message_content text,
    message_time time without time zone,
    admin_id integer,
    response_content text,
    response_time time without time zone,
    response_status character varying,
    PRIMARY KEY (feedback_id)
);
